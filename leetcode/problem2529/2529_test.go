func maximumCount(nums []int) int {
    if nums[0]>0||nums[len(nums)-1]<0{
		return len(nums)
	}

	left,right:=0,len(nums)
	for left<=right {
		mid:=left+(right-left)/2
		if nums[mid]<0{
			left=mid+1
		}else {
			right=mid-1
		}
	}
	negCount:=left
	left,right=0,len(nums)
	for left<=right {
		mid:=left+(right-left)/2
		if mid >= len(nums){
			break
		}
		if nums[mid]<=0{
			left=mid+1
		}else {
			right=mid-1
		}
	}
	posCount:=len(nums)-left
	return max(posCount,negCount)
}

